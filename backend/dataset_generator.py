def get_random_text(length=5):
    return " ".join(random.choices(WORDS, k=length))

def get_random_button_text():
    return random.choice(BUTTON_TEXTS)

def get_random_title():
    return random.choice(TITLE_TEXTS)

def generate_dsl_and_html(pattern_type):
    """Generate paired DSL code and HTML based on pattern type."""
    dsl_tokens = []
    description_parts = []
    
    # Add header
    if random.random() > 0.3:
        num_btns = random.randint(2, 5)
        header_btns = []
        active_idx = random.randint(0, num_btns - 1)
        for i in range(num_btns):
            btn_type = "btn-active" if i == active_idx else "btn-inactive"
            header_btns.append(btn_type)
        dsl_tokens.append(("header", header_btns))
        description_parts.append(f"Navigation header with {num_btns} menu buttons")
    
    # Add rows based on pattern
    num_rows = random.randint(1, 4)
    for _ in range(num_rows):
        if pattern_type == "single_column":
            col_type = "single"
            num_cols = 1
        elif pattern_type == "two_column":
            col_type = "double"
            num_cols = 2
        elif pattern_type == "four_column":
            col_type = "quadruple"
            num_cols = 4
        else:
            col_type = random.choice(["single", "double", "quadruple"])
            num_cols = {"single": 1, "double": 2, "quadruple": 4}[col_type]
        
        columns = []
        for _ in range(num_cols):
            # Each column gets random content
            content_items = []
            num_items = random.randint(1, 3)
            for _ in range(num_items):
                item = random.choice(["big-title", "small-title", "text", "btn-green", "btn-orange", "btn-red"])
                content_items.append(item)
            columns.append((col_type, content_items))
        
        dsl_tokens.append(("row", columns))
        description_parts.append(f"{num_cols}-column row with titles, text and buttons")
    
    # Generate DSL string
    dsl_str = generate_dsl_string(dsl_tokens)
    
    # Generate HTML from DSL
    html_str = generate_html_from_dsl(dsl_tokens)
    
    # Generate description
    description = ". ".join(description_parts) + "."
    
    return dsl_str, html_str, description

def generate_dsl_string(tokens):
    """Convert structured tokens to pix2code DSL format."""
    lines = []
    for token_type, children in tokens:
        lines.append(f"{token_type} {{")
        if token_type == "header":
            for btn in children:
                lines.append(f"  {btn}")
        elif token_type == "row":
            for col_type, content_items in children:
                lines.append(f"  {col_type} {{")
                for item in content_items:
                    lines.append(f"    {item}")
                lines.append("  }")
        lines.append("}")
    return "\n".join(lines)

def generate_html_from_dsl(tokens):
    """Convert structured tokens to HTML using pix2code web DSL mapping."""
    body_content = []
    
    for token_type, children in tokens:
        if token_type == "header":
            btn_html_parts = []
            for btn in children:
                text = get_random_button_text()
                btn_html = WEB_DSL_MAPPING[btn].replace("{text}", text)
                btn_html_parts.append(btn_html)
            header_html = WEB_DSL_MAPPING["header"].replace("{}", "\n      ".join(btn_html_parts))
            body_content.append(header_html)
        
        elif token_type == "row":
            col_htmls = []
            for col_type, content_items in children:
                item_htmls = []
                for item in content_items:
                    if "title" in item:
                        text = get_random_title()
                    elif "btn" in item:
                        text = get_random_button_text()
                    else:
                        text = get_random_text(12)
                    item_html = WEB_DSL_MAPPING[item].replace("{text}", text)
                    item_htmls.append(item_html)
                
                col_html = WEB_DSL_MAPPING[col_type].replace("{}", "\n        ".join(item_htmls))
                col_htmls.append(col_html)
            
            row_html = WEB_DSL_MAPPING["row"].replace("{}", "\n      ".join(col_htmls))
            body_content.append(row_html)
    
    full_html = WEB_DSL_MAPPING["body"].replace("{}", "\n      ".join(body_content))
    return full_html

