    """Generate variations using pix2code DSL patterns."""
    patterns = ["single_column", "two_column", "four_column", "mixed"]
    samples = []
    
    for i in range(count):
        pattern = patterns[i % len(patterns)]
        dsl, html, desc = generate_dsl_and_html(pattern)
        samples.append({
            "id": f"pix2code_{i:03d}",
            "category": f"web_layout_{pattern}",
            "description": desc,
            "dsl_code": dsl,
            "html_code": html,
            "framework": "HTML/Bootstrap",
            "source": "pix2code"
        })
    
    return samples

